#ifndef HW_ID_H
   #define HW_ID_H

#include "AtoD.h"

/*
 *****************************************************************************************
 *		Constants
 *****************************************************************************************
 */
// This assumes a 16bit ADC value.
#define NUM_BITS_IN_HARDWARE_ID         5

/*
 **************************************************************************************************
 *  Prototypes
   --------------------------------------------------------------------------------------------------
 */
inline uint16_t HW_ID_Get( BF_Misc::AtoD* atod_ctrl )
{ return ( ( atod_ctrl->Get_Val() ) >> ( 16 - NUM_BITS_IN_HARDWARE_ID ) ); }


#endif
